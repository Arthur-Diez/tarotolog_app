const getSpreadMeta = (spreadId: string, cardsCount: number): SpreadMeta => {
  const fallback: SpreadMeta = {
    category: "popular",
    tags: ["—Ä–∞—Å–∫–ª–∞–¥", "–∞–Ω–∞–ª–∏–∑", "–∏—Ç–æ–≥"],
    energyCost: Math.max(8, Math.round(cardsCount * 3)),
    popularityScore: 50,
    keywords: []
  };
  return RWS_SPREAD_META[spreadId] ?? fallback;
};

